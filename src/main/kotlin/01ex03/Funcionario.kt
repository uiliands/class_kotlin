class Funcionario {
    var id: Int
    var nome: String
    var cpf: String
    var salario: Double
    var cargo: String

    constructor(id: Int, nome: String, cpf: String, salario: Double, cargo: String) {
        this.id = id
        this.nome = nome
        this.cpf = cpf
        this.salario = salario
        this.cargo = cargo
    }

}